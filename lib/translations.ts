export type MessageKey = keyof typeof messages.ru;
type EnumCategory = "severity" | "projectStatus" | "taskStatus" | "priority" | "direction" | "riskStatus";

export function interpolate(template: string, values?: Record<string, string | number>) {
  if (!values) return template;
  return Object.entries(values).reduce(
    (result, [key, value]) => result.replaceAll(`{${key}}`, String(value)),
    template
  );
}

export function getMessage(locale: Locale, key: MessageKey, values?: Record<string, string | number>) {
  const template = messages[locale][key] ?? messages.ru[key] ?? key;
  return interpolate(template, values);
}

export function getEnumLabel(locale: Locale, category: EnumCategory, value: string) {
  const key = `${category}.${value}` as MessageKey;
  return getMessage(locale, key);
}
